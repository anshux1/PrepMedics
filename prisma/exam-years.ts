  console.log("Seeding users...")
  Exams.forEach(async (item) => {
    const exams = await db.exam.create({
      data: {
        examName: item.title,
        position: item.position
      }
    })
    if (item.title === "NEET") {
      ExamsYears.forEach(async (year) => {
        await db.years.create({
          data: {
            yearName: year.title,
            yearShortName: year.shortName,
            examId: exams.id,
            isVisible: year.isVisible,
          }
        })
      })
    }
  })
  const data = await db.exam.findMany()
